
# Variable declaration and assignment
name: str = "Asif"
age: int = 20
marks: float = 85.5
is_student: bool = True
# Printing variables
print(name)
print(age)
print(marks)
print(is_student)

# Multiple assignment
x, y, z = 10, 20, 30
print(x, y, z)

# Same value to multiple variables
a = b = c = 100
print(a, b, c)

# Variable reassignment
age = 21
print(age)

# Variable with calculated value
total = x + y + z
print(total)
# Variable naming conventions
first_name = "Asif"  # Snake case
FirstName = "Asif"   # Pascal case
firstName = "Asif"   # Camel case
print(first_name, FirstName, firstName)

