# Copy a list using copy() method
fruits = ["apple", "banana", "cherry"]
fruits_copy = fruits.copy()
print(fruits_copy)

# Copy a list using list() method
fruits = ["apple", "banana", "cherry"]
fruits_copy = list(fruits)
print(fruits_copy)

# Copy a list using slice operator
fruits = ["apple", "banana", "cherry"]
fruits_copy = fruits[:]
print(fruits_copy)
