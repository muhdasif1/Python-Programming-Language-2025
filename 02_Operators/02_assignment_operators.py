# ========================================
# Python Assignment Operators
# ========================================

# Simple assignment (=)
x = 10
print("Initial value: x =", x)

# Add AND assignment (+=)
x += 5  # x = x + 5
print("After x += 5:", x)

# Subtract AND assignment (-=)
x -= 3  # x = x - 3
print("After x -= 3:", x)

# Multiply AND assignment (*=)
x *= 2  # x = x * 2
print("After x *= 2:", x)

# Divide AND assignment (/=)
x /= 4  # x = x / 4
print("After x /= 4:", x)

# Floor divide AND assignment (//=)
x //= 2  # x = x // 2
print("After x //= 2:", x)

# Modulus AND assignment (%=)
x %= 3  # x = x % 3
print("After x %= 3:", x)

# Exponent AND assignment (**=)
x **= 3  # x = x ** 3
print("After x **= 3:", x)

# Bitwise AND assignment (&=)
x &= 2  # x = x & 2
print("After x &= 2:", x)

# Bitwise OR assignment (|=)
x |= 1  # x = x | 1
print("After x |= 1:", x)

# Bitwise XOR assignment (^=)
x ^= 3  # x = x ^ 3
print("After x ^= 3:", x)

# Left Shift assignment (<<=)
x <<= 2  # x = x << 2
print("After x <<= 2:", x)

# Right Shift assignment (>>=)
x >>= 1  # x = x >> 1
print("After x >>= 1:", x)
