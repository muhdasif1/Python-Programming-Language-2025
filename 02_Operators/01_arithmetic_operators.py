# ========================================
# Python Arithmetic Operators
# ========================================

# Declare two numbers
a = 10
b = 3

# Addition
add = a + b
print("Addition (a + b):", add)  # 10 + 3 = 13

# Subtraction
sub = a - b
print("Subtraction (a - b):", sub)  # 10 - 3 = 7

# Multiplication
mul = a * b
print("Multiplication (a * b):", mul)  # 10 * 3 = 30

# Division (returns float)
div = a / b
print("Division (a / b):", div)  # 10 / 3 â‰ˆ 3.3333

# Floor Division (returns integer part only)
floor_div = a // b
print("Floor Division (a // b):", floor_div)  # 10 // 3 = 3

# Modulus (remainder)
mod = a % b
print("Modulus (a % b):", mod)  # 10 % 3 = 1

# Exponentiation (power)
exp = a ** b
print("Exponentiation (a ** b):", exp)  # 10 ** 3 = 1000
