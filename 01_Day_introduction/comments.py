# Comments
# Comments play a crucial role in enhancing code readability and allowing developers to leave notes within their code. 
# In Python, any text preceded by a hash (#) symbol is considered a comment and is not executed when the code runs.

# 1) Example: Single Line Comment
    # This is the first comment
    # This is the second comment
    # Python is eating the world

# 2) Multiline Comment
# Triple quote can be used for multiline comment if it is not assigned to a variable
"""This is multiline comment
multiline comment takes multiple lines.
python is eating the world
"""